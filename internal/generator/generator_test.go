func TestGenerate_MarkdownTargets(t *testing.T) {
	m := fs.NewMemFS()
	ws := &config.Workspace{Name: "Test", ActiveContext: "work"}

	content := "<!-- Generated by devkit -->\n\nHello world.\n"
	r, err := Generate(m, "/project", content, ws, "/templates")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	wantWritten := len(MarkdownTargets) + len(MDCTargets)
	if len(r.Written) != wantWritten {
		t.Fatalf("wrote %d files, want %d", len(r.Written), wantWritten)
	}

	for _, name := range MarkdownTargets {
		data, err := m.ReadFile("/project/" + name)
		if err != nil {
			t.Fatalf("missing %s: %v", name, err)
		}
		if string(data) != content {
			t.Fatalf("%s content mismatch", name)
		}
	}
}

func TestGenerate_OverwriteReporting(t *testing.T) {
	m := fs.NewMemFS()
	if err := m.WriteFile("/project/CLAUDE.md", []byte("old content"), 0644); err != nil {
		t.Fatal(err)
	}
	ws := &config.Workspace{Name: "Test", ActiveContext: "work"}

	content := "new content"
	r, err := Generate(m, "/project", content, ws, "/templates")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	if len(r.Overwritten) == 0 {
		t.Fatal("expected overwrite to be reported")
	}
	found := false
	for _, name := range r.Overwritten {
		if name == "CLAUDE.md" {
			found = true
		}
	}
	if !found {
		t.Fatal("CLAUDE.md should be in overwritten list")
	}
}

func TestGenerate_StructuredTargets(t *testing.T) {
	m := fs.NewMemFS()
	if err := m.WriteFile("/templates/opencode.json.tmpl", []byte("{\"context\": \"{{.Workspace.Name}}\"}\n"), 0644); err != nil {
		t.Fatal(err)
	}
	ws := &config.Workspace{Name: "Dev", ActiveContext: "work"}

	r, err := Generate(m, "/project", "composed content", ws, "/templates")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	foundOpencode := false
	for _, name := range r.Written {
		if name == "opencode.json" {
			foundOpencode = true
		}
	}
	if !foundOpencode {
		t.Fatal("opencode.json should be written when template exists")
	}

	data, err := m.ReadFile("/project/opencode.json")
	if err != nil {
		t.Fatalf("missing opencode.json: %v", err)
	}
	if string(data) != "{\"context\": \"Dev\"}\n" {
		t.Fatalf("opencode.json content = %q", string(data))
	}
}

