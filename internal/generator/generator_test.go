func TestGenerate_MDCTarget(t *testing.T) {
	m := fs.NewMemFS()
	ws := &config.Workspace{Name: "Test", ActiveContext: "work"}

	content := "<!-- Generated by devkit -->\n\nHello world.\n"
	_, err := Generate(m, "/project", content, ws, "/templates")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	mdcPath := "/project/.cursor/rules/devkit-context.mdc"
	if !m.Exists(mdcPath) {
		t.Fatalf("expected %s to be written", mdcPath)
	}

	data, err := m.ReadFile(mdcPath)
	if err != nil {
		t.Fatalf("reading mdc file: %v", err)
	}

	if !strings.Contains(string(data), "alwaysApply: true") {
		t.Fatalf("mdc file missing alwaysApply frontmatter, got: %s", string(data))
	}
	if !strings.Contains(string(data), content) {
		t.Fatalf("mdc file missing content, got: %s", string(data))
	}
}

func TestGenerateNoSliceMutation(t *testing.T) {
	original := make([]string, len(MarkdownTargets))
	copy(original, MarkdownTargets)

	fsys := fs.NewMemFS()
	if err := fsys.MkdirAll("/target", 0755); err != nil {
		t.Fatal(err)
	}

	ws := &config.Workspace{
		Name:          "test",
		ActiveContext: "work",
		ExtraTargets:  []string{"extra1.md", "extra2.md"},
	}

	if _, err := Generate(fsys, "/target", "test content", ws, "/templates"); err != nil {
		t.Fatal(err)
	}

	if !reflect.DeepEqual(MarkdownTargets, original) {
		t.Errorf("Generate mutated MarkdownTargets: got %v, want %v", MarkdownTargets, original)
	}
}

func TestGenerateRejectsPathTraversal(t *testing.T) {
	fsys := fs.NewMemFS()
	if err := fsys.MkdirAll("/target", 0755); err != nil {
		t.Fatal(err)
	}

	ws := &config.Workspace{
		Name:          "test",
		ActiveContext: "work",
		ExtraTargets:  []string{"../../etc/passwd"},
	}

	_, err := Generate(fsys, "/target", "content", ws, "/templates")
	if err == nil {
		t.Fatal("expected error for path traversal in extra_targets, got nil")
	}
	if !strings.Contains(err.Error(), "escapes") {
		t.Errorf("expected 'escapes' in error, got: %v", err)
	}
}

func TestGenerate_ExtraTargets(t *testing.T) {
	m := fs.NewMemFS()
	ws := &config.Workspace{
		Name:          "Test",
		ActiveContext: "work",
		ExtraTargets:  []string{"custom/tool.md", ".amp/context.md"},
	}

	content := "<!-- Generated by devkit -->\n\nHello world.\n"
	r, err := Generate(m, "/project", content, ws, "/templates")
	if err != nil {
		t.Fatalf("Generate: %v", err)
	}

	for _, extra := range ws.ExtraTargets {
		path := "/project/" + extra
		if !m.Exists(path) {
			t.Fatalf("expected extra target %s to be written", path)
		}
		data, err := m.ReadFile(path)
		if err != nil {
			t.Fatalf("reading %s: %v", extra, err)
		}
		if string(data) != content {
			t.Fatalf("%s content mismatch", extra)
		}
	}

	foundCustom := false
	for _, name := range r.Written {
		if name == "custom/tool.md" {
			foundCustom = true
		}
	}
	if !foundCustom {
		t.Fatal("custom/tool.md should be in written list")
	}
}
